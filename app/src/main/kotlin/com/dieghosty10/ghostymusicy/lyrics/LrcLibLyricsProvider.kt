package com.dieghosty10.ghostymusicy.lyrics

import android.content.Context
import com.dieghosty10.ghostymusicy.lrclib.LrcLib
import com.dieghosty10.ghostymusicy.constants.EnableLrcLibKey
import com.dieghosty10.ghostymusicy.utils.dataStore
import com.dieghosty10.ghostymusicy.utils.get

object LrcLibLyricsProvider : LyricsProvider {
    override val name = "LrcLib"

    override fun isEnabled(context: Context): Boolean = context.dataStore[EnableLrcLibKey] ?: true

    override suspend fun getLyrics(
        id: String,
        title: String,
        artist: String,
        album: String?,
        duration: Int,
    ): Result<String> = LrcLib.getLyrics(title, artist, duration)

    override suspend fun getAllLyrics(
        id: String,
        title: String,
        artist: String,
        album: String?,
        duration: Int,
        callback: (String) -> Unit,
    ) {
        LrcLib.getAllLyrics(title, artist, duration, null, callback)
    }
}
