package com.dieghosty10.ghostymusicy.lyrics

import android.content.Context
import com.dieghosty10.ghostymusicy.constants.EnableSimpMusicLyricsKey
import com.dieghosty10.ghostymusicy.simpmusic.SimpMusicLyrics
import com.dieghosty10.ghostymusicy.utils.dataStore
import com.dieghosty10.ghostymusicy.utils.get

object SimpMusicLyricsProvider : LyricsProvider {
    override val name: String = "SimpMusic"

    override fun isEnabled(context: Context): Boolean =
        context.dataStore[EnableSimpMusicLyricsKey] ?: true

    override suspend fun getLyrics(
        id: String,
        title: String,
        artist: String,
        album: String?,
        duration: Int,
    ): Result<String> = SimpMusicLyrics.getLyrics(videoId = id, duration = duration)

    override suspend fun getAllLyrics(
        id: String,
        title: String,
        artist: String,
        album: String?,
        duration: Int,
        callback: (String) -> Unit,
    ) {
        SimpMusicLyrics.getAllLyrics(videoId = id, duration = duration, callback = callback)
    }
}

